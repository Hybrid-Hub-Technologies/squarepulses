// Generate post from manual signal data input
async function generateSignalPost() {
  const coin = document.getElementById('sig-coin').value.toUpperCase().trim();
  const entry = parseFloat(document.getElementById('sig-entry').value);
  const tp1 = parseFloat(document.getElementById('sig-tp1').value);
  const tp2 = parseFloat(document.getElementById('sig-tp2').value);
  const sl = parseFloat(document.getElementById('sig-sl').value);
  const riskType = document.getElementById('sig-riskType').value;

  if (!coin || !entry || !tp1 || !tp2 || !sl) {
    showToast('error', '❌', 'Fill all signal data fields');
    return;
  }

  try {
    const systemPrompt = `You are a professional crypto trader writing viral posts for Binance Square. 
Create an engaging, concise post (max 200 chars) with:
- The trade setup (coin, entry, targets)
- Risk/reward ratio
- Action-oriented language
- Relevant emojis
Format: $COIN Entry: $X TP1: $Y TP2: $Z SL: $W`;

    const riskLabel = riskType === 'scalp' ? 'Scalp' : riskType === 'swing' ? 'Swing' : 'Long';
    const rr = ((tp2 - entry) / (entry - sl)).toFixed(2);

    const userPrompt = `Create a trading signal post:
- Coin: $${coin}
- Entry: $${entry}
- TP1: $${tp1}
- TP2: $${tp2}
- SL: $${sl}
- Type: ${riskLabel} (RR: 1:${rr})
Make it sound professional and exciting!`;

    showToast('info', '⏳', 'AI generating post...');

    // Try to call backend Claude endpoint
    const res = await fetch('http://localhost:5000/api/claude', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ systemPrompt, userPrompt: userPrompt })
    });

    if (res.ok) {
      const data = await res.json();
      const generatedText = data.message || data.text || '';
      
      // Format the full post with trade data
      const fullPost = `$${coin} Entry: $${entry} TP1: $${tp1} TP2: $${tp2} SL: $${sl}\n\n${generatedText}`;
      
      document.getElementById('sig-postContent').value = fullPost;
      syncComposer(document.getElementById('sig-postContent'));
      showToast('success', '✨', 'Post generated by AI!');
    } else {
      throw new Error('AI generation failed');
    }
  } catch (e) {
    console.error('Generate error:', e);
    // Fallback to template
    const template = `🎯 SIGNAL: $${coin}
Entry: $${entry}
🎁 TP1: $${tp1}
🚀 TP2: $${tp2}
🛑 SL: $${sl}

Risk/Reward looks good! Ready to execute? 📈`;
    document.getElementById('sig-postContent').value = template;
    syncComposer(document.getElementById('sig-postContent'));
    showToast('info', '📊', 'Template signal generated');
  }
}

// Fetch last saved post from database and populate fields
async function fetchLastPost() {
  try {
    showToast('info', '⏳', 'Loading last post...');
    
    const res = await fetch('http://localhost:5000/api/posts?page=1');
    const data = await res.json();
    
    if (data.posts && data.posts.length > 0) {
      const lastPost = data.posts[0]; // Most recent
      
      // Populate the signal data inputs
      document.getElementById('sig-coin').value = lastPost.coin_symbol || '';
      document.getElementById('sig-entry').value = lastPost.entry_price || '';
      document.getElementById('sig-tp1').value = lastPost.tp1 || '';
      document.getElementById('sig-tp2').value = lastPost.tp2 || '';
      document.getElementById('sig-sl').value = lastPost.sl || '';
      
      showToast('success', '📋', `Loaded: $${lastPost.coin_symbol}`);
    } else {
      showToast('info', '📭', 'No posts found in database');
    }
  } catch (e) {
    console.error('Fetch error:', e);
    showToast('error', '❌', 'Could not load posts');
  }
}
